#!/usr/bin/env python3
"""
Prepare dataset for YOLO training from Label Studio export.

Matches label files (with UUID prefix) to images, splits into train/val,
and writes the data.yaml file.

Usage:
  python prepare_dataset.py
"""

import shutil
import random
from pathlib import Path

# ---------------------------------------------------------------------------
# Config
# ---------------------------------------------------------------------------

LABELS_DIR   = Path("label-studio-project/labels")
IMAGES_DIR   = Path("old-snaps")
OUTPUT_DIR   = Path("dataset")
VAL_SPLIT    = 0.2   # 20% validation
RANDOM_SEED  = 42

# Must match notes.json order (index = class id)
CLASS_NAMES  = ["Airplane", "Car", "chicken", "egg"]

# ---------------------------------------------------------------------------
# Match labels to images
# ---------------------------------------------------------------------------

def find_image(label_path: Path, images_dir: Path) -> Path | None:
    """
    Label files have a UUID prefix: `c0cb3bd2-lastsnap.txt`
    Images don't:                   `lastsnap.jpg`
    Strip everything up to and including the first `-`.
    """
    # Strip UUID prefix (first 9 chars: 8 hex + dash)
    stem = label_path.stem  # e.g. "c0cb3bd2-lastsnap"
    # Find first dash and strip everything before+including it
    if "-" in stem:
        image_stem = stem[stem.index("-") + 1:]  # e.g. "lastsnap"
    else:
        image_stem = stem

    for ext in [".jpg", ".jpeg", ".png"]:
        candidate = images_dir / f"{image_stem}{ext}"
        if candidate.exists():
            return candidate

    return None

# ---------------------------------------------------------------------------
# Main
# ---------------------------------------------------------------------------

def main():
    label_files = sorted(LABELS_DIR.glob("*.txt"))
    print(f"Found {len(label_files)} label files")

    # Match labels → images
    pairs = []
    unmatched = []
    for lf in label_files:
        img = find_image(lf, IMAGES_DIR)
        if img:
            pairs.append((lf, img))
        else:
            unmatched.append(lf)

    print(f"Matched:   {len(pairs)}")
    if unmatched:
        print(f"Unmatched: {len(unmatched)}")
        for u in unmatched:
            print(f"  ✗ {u.name}")

    if not pairs:
        print("No matched pairs found — check LABELS_DIR and IMAGES_DIR paths")
        return

    # Split train/val
    random.seed(RANDOM_SEED)
    random.shuffle(pairs)
    val_count   = max(1, int(len(pairs) * VAL_SPLIT))
    val_pairs   = pairs[:val_count]
    train_pairs = pairs[val_count:]
    print(f"Train: {len(train_pairs)}  Val: {len(val_pairs)}")

    # Create output dirs
    for split in ["train", "val"]:
        (OUTPUT_DIR / "images" / split).mkdir(parents=True, exist_ok=True)
        (OUTPUT_DIR / "labels" / split).mkdir(parents=True, exist_ok=True)

    # Copy files
    for split, split_pairs in [("train", train_pairs), ("val", val_pairs)]:
        for label_path, image_path in split_pairs:
            # Use the image stem as the canonical filename (drop UUID prefix)
            dest_img   = OUTPUT_DIR / "images" / split / image_path.name
            dest_label = OUTPUT_DIR / "labels" / split / f"{image_path.stem}.txt"
            shutil.copy2(image_path, dest_img)
            shutil.copy2(label_path, dest_label)

    # Write data.yaml
    yaml_path = OUTPUT_DIR / "data.yaml"
    yaml_path.write_text(f"""# Auto-generated by prepare_dataset.py
path: {OUTPUT_DIR.resolve()}
train: images/train
val: images/val

nc: {len(CLASS_NAMES)}
names: {CLASS_NAMES}
""")

    print(f"\n✓ Dataset written to {OUTPUT_DIR}/")
    print(f"✓ data.yaml written to {yaml_path}")
    print(f"\nNext steps:")
    print(f"  pip install ultralytics")
    print(f"  yolo train model=yolov8n.pt data={yaml_path} epochs=50 imgsz=640")
    print(f"\nAfter training, your model will be at:")
    print(f"  runs/detect/train/weights/best.pt")
    print(f"\nThen update config.json:")
    print(f'  "model_path": "runs/detect/train/weights/best.pt"')

if __name__ == "__main__":
    main()
